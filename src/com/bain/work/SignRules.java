package com.bain.work;

public class SignRules {
	private long id;
	private int continuesDays;
	private int type;
	private int award;
	private long updateTime;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public int getContinuesDays() {
		return continuesDays;
	}
	public void setContinuesDays(int continuesDays) {
		this.continuesDays = continuesDays;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getAward() {
		return award;
	}
	public void setAward(int award) {
		this.award = award;
	}
	public long getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(long updateTime) {
		this.updateTime = updateTime;
	}
	
}
